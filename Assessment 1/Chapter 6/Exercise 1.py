import math

#ceil a number rounds it up to the nearest integer
a = 2.3
a_ceil = math.ceil(a)  # ceil function
print(f"Ceil of {a} is: {a_ceil}")

#floor of a number rounding it down to the nearest integer
a_floor = math.floor(a)  # floor function
print(f"Floor of {a} is: {a_floor}")

#factorial of the number
a = 5
a_factorial = math.factorial(a)  # factorial function
print(f"Factorial of {a} is: {a_factorial}")

#absolute value of the number
value = -2**3
value_absolute = abs(value)  # abs function
print(f"Absolute value of {value} is: {value_absolute}")

#square root the number
a = 16
squareroot_a = math.sqrt(a)  # square root function
print(f"Square root of {a} is: {squareroot_a}")
